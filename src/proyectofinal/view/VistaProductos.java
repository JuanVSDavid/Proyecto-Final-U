    @Override
    public void paint(Graphics grafica){
        super.paint(grafica);
        int cantidadParaAseo = ProductoController.getCantidadParaAseo();
        int cantidadParaMascota = ProductoController.getCantidadParaMascota();
        int cantidadParaOtros = ProductoController.getCantidadParaOtros();
        int cantidadParaPapeleria = ProductoController.getCantidadParaPapeleria();
        int cantidadParaViveres = ProductoController.getCantidadParaViveres();
        if(cantidadParaAseo > 0 || cantidadParaMascota > 0 || cantidadParaOtros > 0 || cantidadParaPapeleria > 0 || cantidadParaViveres > 0){
            int total = cantidadParaAseo + cantidadParaMascota + cantidadParaOtros + cantidadParaPapeleria + cantidadParaViveres;
            int gradosAseo = cantidadParaAseo * 360 / total;
            int gradosMascota = cantidadParaMascota * 360 / total;
            int gradosOtros = cantidadParaOtros * 360 / total;
            int gradosPapeleria = cantidadParaPapeleria * 360 / total;
            int gradosViveres = cantidadParaViveres * 360 / total;
            
            grafica.setColor(Color.BLUE);
            grafica.fillArc(panelGrafico.getX() + 75, panelGrafico.getY() + 100, panelGrafico.getWidth() / 2, panelGrafico.getWidth() / 2, 0, gradosAseo);
            grafica.setColor(Color.red);
            grafica.fillArc(panelGrafico.getX() + 75, panelGrafico.getY() + 100, panelGrafico.getWidth() / 2, panelGrafico.getWidth() / 2, gradosAseo, gradosMascota);
            grafica.setColor(Color.CYAN);
            grafica.fillArc(panelGrafico.getX() + 75, panelGrafico.getY() + 100, panelGrafico.getWidth() / 2, panelGrafico.getWidth() / 2, gradosAseo + gradosMascota, gradosOtros);
            grafica.setColor(Color.GRAY);
            grafica.fillArc(panelGrafico.getX() + 75, panelGrafico.getY() + 100, panelGrafico.getWidth() / 2, panelGrafico.getWidth() / 2, gradosAseo + gradosMascota + gradosOtros, gradosPapeleria);
            grafica.setColor(Color.ORANGE);
            grafica.fillArc(panelGrafico.getX() + 75, panelGrafico.getY() + 100, panelGrafico.getWidth() / 2, panelGrafico.getWidth() / 2, gradosAseo + gradosMascota + gradosOtros + gradosPapeleria, gradosViveres);
        }
    }
    
